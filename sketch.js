$(document).ready(function(){
    $(".숙제").mouseover(function(){
        var image = $(".숙제i");
    $(".숙제i").show();
    $(".숙제i").stop().animate({top:window.innerHeight/2-image.innerHeight()/2});
    });
    $(".숙제").mouseout(function(){
    $(".숙제i").stop().animate({top:"100%"});
    });
    });

